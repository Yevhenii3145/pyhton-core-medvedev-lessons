# строка-неизменяемая, упорядоченная коллекция НЕ уникальных элементов
hello_str = "Hello"
print(hello_str.upper())  # HELLO
print(hello_str)  # Hello
print(hello_str.lower())  # hello

new_string = "Hello World"
print(new_string.startswith("HEL"))  # False
print(new_string.endswith("orld"))  # True
